use anyhow::{Context, Result, bail};
use rive_binary::{RuntimeFile, read_runtime_file};
use std::collections::{BTreeMap, BTreeSet};
use std::env;
use std::path::{Path, PathBuf};

fn main() {
    if let Err(error) = run() {
        eprintln!("generate-corpus error: {error:#}");
        std::process::exit(1);
    }
}

fn run() -> Result<()> {
    let options = Options::parse(env::args().skip(1).collect())?;
    let existing = match options.existing.as_ref() {
        Some(path) if path.exists() => parse_existing(path)?,
        _ => BTreeMap::new(),
    };

    let mut asset_paths = std::fs::read_dir(&options.assets_dir)
        .with_context(|| format!("failed to read {}", options.assets_dir.display()))?
        .filter_map(|entry| entry.ok())
        .map(|entry| entry.path())
        .filter(|path| path.extension().is_some_and(|extension| extension == "riv"))
        .collect::<Vec<_>>();
    asset_paths.sort();

    let mut output = String::from(
        "# Golden corpus manifest for the V2 end-to-end render stream ratchet.\n\
         # Paths are relative to RIVE_RUNTIME_DIR unless absolute.\n\
         # Generated by: cargo run -p golden-compare --bin generate-corpus -- --assets-dir <dir> --existing corpus.toml --out corpus.toml\n\n",
    );

    for path in asset_paths {
        let file_name = path
            .file_name()
            .and_then(|name| name.to_str())
            .with_context(|| format!("non-utf8 asset path {}", path.display()))?;
        let id = file_name
            .strip_suffix(".riv")
            .unwrap_or(file_name)
            .to_owned();
        let previous = existing.get(&id);
        let bytes =
            std::fs::read(&path).with_context(|| format!("failed to read {}", path.display()))?;
        let (status, mut features) = match read_runtime_file(&bytes) {
            Ok(runtime) => (
                previous
                    .map(|entry| entry.status.as_str())
                    .unwrap_or("not-yet")
                    .to_owned(),
                type_key_features(&runtime),
            ),
            Err(error) => (
                "unsupported-feature".to_owned(),
                vec![format!(
                    "import-error:{}",
                    normalize_feature(&error.to_string())
                )],
            ),
        };
        if previous.is_some_and(|entry| entry.status == "unsupported-feature") {
            for feature in previous
                .into_iter()
                .flat_map(|entry| entry.features.iter())
                .filter(|feature| !feature.starts_with("type-key:"))
            {
                if !features.contains(feature) {
                    features.push(feature.clone());
                }
            }
            features.sort();
        }
        let relative_path = options.relative_path(file_name);

        output.push_str("[[file]]\n");
        output.push_str(&format!("id = {}\n", quoted(&id)));
        output.push_str(&format!("path = {}\n", quoted(&relative_path)));
        if let Some(artboard) = previous.and_then(|entry| entry.artboard.as_ref()) {
            output.push_str(&format!("artboard = {}\n", quoted(artboard)));
        }
        if let Some(state_machine) = previous.and_then(|entry| entry.state_machine.as_ref()) {
            output.push_str(&format!("state_machine = {}\n", quoted(state_machine)));
        }
        if let Some(input_script) = previous.and_then(|entry| entry.input_script.as_ref()) {
            output.push_str(&format!("input_script = {}\n", quoted(input_script)));
        }
        let samples = previous
            .map(|entry| entry.samples.clone())
            .unwrap_or_else(|| vec!["0.0".to_owned()]);
        output.push_str(&format!("samples = [{}]\n", samples.join(", ")));
        output.push_str(&format!("status = {}\n", quoted(&status)));
        if let Some(milestone) = previous.and_then(|entry| entry.milestone.as_ref()) {
            output.push_str(&format!("milestone = {}\n", quoted(milestone)));
        }
        output.push_str("features = [");
        for (index, feature) in features.iter().enumerate() {
            if index != 0 {
                output.push_str(", ");
            }
            output.push_str(&quoted(feature));
        }
        output.push_str("]\n\n");
    }

    match options.out {
        Some(path) => std::fs::write(&path, output)
            .with_context(|| format!("failed to write {}", path.display()))?,
        None => print!("{output}"),
    }

    Ok(())
}

#[derive(Debug)]
struct Options {
    assets_dir: PathBuf,
    existing: Option<PathBuf>,
    out: Option<PathBuf>,
    relative_prefix: String,
}

impl Options {
    fn parse(args: Vec<String>) -> Result<Self> {
        let mut assets_dir = None;
        let mut existing = None;
        let mut out = None;
        let mut relative_prefix = "tests/unit_tests/assets".to_owned();

        let mut index = 0;
        while index < args.len() {
            let arg = &args[index];
            let mut value = |option: &str| -> Result<String> {
                index += 1;
                args.get(index)
                    .cloned()
                    .with_context(|| format!("{option} requires a value"))
            };

            match arg.as_str() {
                "--assets-dir" => assets_dir = Some(PathBuf::from(value(arg)?)),
                "--existing" => existing = Some(PathBuf::from(value(arg)?)),
                "--out" => out = Some(PathBuf::from(value(arg)?)),
                "--relative-prefix" => relative_prefix = value(arg)?,
                "--help" | "-h" => {
                    println!(
                        "usage: generate-corpus --assets-dir <dir> [--existing corpus.toml] [--out corpus.toml]"
                    );
                    std::process::exit(0);
                }
                other => bail!("unknown option: {other}"),
            }
            index += 1;
        }

        Ok(Self {
            assets_dir: assets_dir.context("missing --assets-dir <dir>")?,
            existing,
            out,
            relative_prefix,
        })
    }

    fn relative_path(&self, file_name: &str) -> String {
        format!("{}/{file_name}", self.relative_prefix.trim_end_matches('/'))
    }
}

#[derive(Debug, Clone, Default)]
struct ExistingEntry {
    artboard: Option<String>,
    state_machine: Option<String>,
    input_script: Option<String>,
    samples: Vec<String>,
    status: String,
    milestone: Option<String>,
    features: Vec<String>,
}

fn parse_existing(path: &Path) -> Result<BTreeMap<String, ExistingEntry>> {
    let text = std::fs::read_to_string(path)
        .with_context(|| format!("failed to read existing {}", path.display()))?;
    let mut entries = BTreeMap::new();
    let mut id = None::<String>;
    let mut entry = ExistingEntry::default();

    for raw_line in text.lines() {
        let line = raw_line.split('#').next().unwrap_or("").trim();
        if line.is_empty() {
            continue;
        }
        if line == "[[file]]" {
            if let Some(id) = id.take() {
                entries.insert(id, entry);
                entry = ExistingEntry::default();
            }
            continue;
        }
        let Some((key, value)) = line.split_once('=') else {
            continue;
        };
        let key = key.trim();
        let value = value.trim();
        match key {
            "id" => id = Some(parse_string(value)?),
            "artboard" => entry.artboard = Some(parse_string(value)?),
            "state_machine" => entry.state_machine = Some(parse_string(value)?),
            "input_script" => entry.input_script = Some(parse_string(value)?),
            "samples" => entry.samples = parse_array(value).unwrap_or_default(),
            "status" => entry.status = parse_string(value)?,
            "milestone" => entry.milestone = Some(parse_string(value)?),
            "features" => entry.features = parse_string_array(value).unwrap_or_default(),
            _ => {}
        }
    }
    if let Some(id) = id.take() {
        entries.insert(id, entry);
    }
    Ok(entries)
}

fn type_key_features(file: &RuntimeFile) -> Vec<String> {
    let mut features = BTreeSet::new();
    for object in file.objects.iter().flatten() {
        features.insert(format!("type-key:{}:{}", object.type_key, object.type_name));
    }
    features.into_iter().collect()
}

fn normalize_feature(value: &str) -> String {
    value
        .chars()
        .map(|ch| if ch.is_ascii_alphanumeric() { ch } else { '-' })
        .collect::<String>()
        .split('-')
        .filter(|part| !part.is_empty())
        .collect::<Vec<_>>()
        .join("-")
}

fn parse_array(value: &str) -> Result<Vec<String>> {
    let value = value.trim();
    if !value.starts_with('[') || !value.ends_with(']') {
        bail!("expected array");
    }
    let inner = &value[1..value.len() - 1];
    if inner.trim().is_empty() {
        return Ok(Vec::new());
    }
    Ok(inner
        .split(',')
        .map(|part| part.trim().to_owned())
        .collect())
}

fn parse_string_array(value: &str) -> Result<Vec<String>> {
    parse_array(value)?
        .into_iter()
        .map(|value| parse_string(&value))
        .collect()
}

fn parse_string(value: &str) -> Result<String> {
    let bytes = value.as_bytes();
    if bytes.len() < 2 || bytes[0] != b'"' || bytes[bytes.len() - 1] != b'"' {
        bail!("expected quoted string");
    }
    let mut out = String::new();
    let mut escaped = false;
    for ch in value[1..value.len() - 1].chars() {
        if escaped {
            out.push(match ch {
                'n' => '\n',
                'r' => '\r',
                't' => '\t',
                '\\' => '\\',
                '"' => '"',
                other => other,
            });
            escaped = false;
        } else if ch == '\\' {
            escaped = true;
        } else {
            out.push(ch);
        }
    }
    if escaped {
        bail!("dangling string escape");
    }
    Ok(out)
}

fn quoted(value: &str) -> String {
    let mut out = String::from("\"");
    for ch in value.chars() {
        match ch {
            '\\' => out.push_str("\\\\"),
            '"' => out.push_str("\\\""),
            '\n' => out.push_str("\\n"),
            '\r' => out.push_str("\\r"),
            '\t' => out.push_str("\\t"),
            _ => out.push(ch),
        }
    }
    out.push('"');
    out
}
